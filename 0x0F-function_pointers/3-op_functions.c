#include"3-calc.h"

/**
 * op_add - add a and b
 * @a:int 1
 * @b:int 2
 * Return: a + b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub a and b
 * @a: int 1
 * @b: int 2
 * Return: a - b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mull a and b
 * @a: int 1
 * @:
 *  int 2
 *  Return: a*b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a and b
 * @a: int 1
 * @b: int 2
 * Return: a / b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modul of a and b
 * @a: int 1
 * @b: int 2
 * Return: a % b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
